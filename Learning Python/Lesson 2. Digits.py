# операции с числамию
# целочисленное деление
print (40/5)
print (41/5)

# отличие в делении на целое/ на десятичное
print(int(10/4))
print(10/4.0)

# получение остатка от целочисленного деления (получаем кол-во знаков после запятой)
print (41%5)
print (45%8)

# возведение в степень

print (2**3)

# e-1 --> 10 в степени (-1)
print (2e-1)
# e2 --> 10 в степени 2
print (2e2)

